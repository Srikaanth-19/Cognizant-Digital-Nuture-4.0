// 6. Data Type Demonstration
// Objective: Understand Java's primitive data types.

public class DataTypeDemonstration {
    public static void main(String[] args) {
        int num = 98;
        float rate = 16.5f;
        double value = 187.006;
        char grade = 'C';
        boolean status = true;

        System.out.println("Integer: " + num);
        System.out.println("Float: " + rate);
        System.out.println("Double: " + value);
        System.out.println("Character: " + grade);
        System.out.println("Boolean: " + status);
    }
}
